function სიმეტრიულისხვაობა(ნაკრები1, ნაკრები2) {
    const განსხვავება = new Set(
      [...ნაკრები1].filter(ელემენტი => !ნაკრები2.has(ელემენტი))
    );
  
    ნაკრები2.forEach(ელემენტი => {
      if (!ნაკრები1.has(ელემენტი)) {
        განსხვავება.add(ელემენტი);
      }
    });
  
    return განსხვავება;
  }
  

